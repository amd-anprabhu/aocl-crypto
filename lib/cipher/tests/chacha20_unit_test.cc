TEST(Chacha20, QuarterRoundTest)
{
    Uint32 a = 0x11111111;
    Uint32 b = 0x01020304;
    Uint32 c = 0x9b8d6f43;
    Uint32 d = 0x01234567;
    QuarterRound(a, b, c, d);
    EXPECT_EQ(a, 0xea2a92f4LLU);
    EXPECT_EQ(b, 0xcb1cf8ceLLU);
    EXPECT_EQ(c, 0x4581472eLLU);
    EXPECT_EQ(d, 0x5881c4bbLLU);
}

